class TV:
    def turn_on(self):
        print("TV: Turning on...")

    def turn_off(self):
        print("TV: Turning off...")

    def change_channel(self, channel):
        print(f"TV: Changing to channel {channel}")


class DVDPlayer:
    def turn_on(self):
        print("DVD Player: Turning on...")

    def turn_off(self):
        print("DVD Player: Turning off...")

    def play(self):
        print("DVD Player: Playing DVD...")

    def stop(self):
        print("DVD Player: Stopping DVD...")


class SoundSystem:
    def turn_on(self):
        print("Sound System: Turning on...")

    def turn_off(self):
        print("Sound System: Turning off...")

    def set_volume(self, volume):
        print(f"Sound System: Setting volume to {volume}")
